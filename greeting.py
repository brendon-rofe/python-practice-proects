# Ask for the user's name
user_name = input("What is your name? ")

# Ask for the user's age
user_age = input("How old are you? ")

# Calculate the age in 5 years
age_in_5_years = int(user_age) + 5

# Print out a greeting and tell the user how old they will be in 5 years
print(f'Greeting {user_name}! You will be {age_in_5_years} in 5 years!')
